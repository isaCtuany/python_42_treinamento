#!/usr/bin/python3 

#Digite um número
first_number = int(input("Give me the first number: "))
second_number = int(input("Give me the second number: "))
print("thank you!")

#Cálculos
soma = (first_number + second_number)
sub = (first_number - second_number)
div = (first_number // second_number)
multi = (first_number * second_number)

#Result of their addition, subtraction, division, and multiplication.
print(f"{first_number} + {second_number} = {soma}")
print(f"{first_number} - {second_number} = {sub}")
print(f"{first_number} / {second_number} = {div}")
print(f"{first_number} * {second_number} = {multi}")






